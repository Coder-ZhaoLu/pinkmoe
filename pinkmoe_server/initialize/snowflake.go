package initialize

import (
	"time"

	sf "github.com/bwmarrin/snowflake"
)

func SnowflakeInit(startTime string, machineID int64) (node *sf.Node) {
	var st time.Time
	st, err := time.Parse("2006-01-02", startTime)
	if err != nil {
		return
	}
	sf.Epoch = st.UnixNano() / 1000000
	node, err = sf.NewNode(machineID)
	return node
}
